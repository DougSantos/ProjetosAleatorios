    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            Funcionario f = new Funcionario();
            f.setMatricula(Integer.parseInt(jTextFieldMatricula.getText()));
            f.setNome(jTextFieldnomeFuncionario.getText());
            int posicaoSelecionadaTabela = jTableSetor.getSelectedRow();
            Setor s = this.listaSetoresCadastrados.get(posicaoSelecionadaTabela);
            f.setSetor(s);
            Fachada fachada = new Fachada();
            fachada.cadastrar(f);
            JOptionPane.showMessageDialog(rootPane, "Funcion치rio cadastrado com sucesso");
            ArrayList<Funcionario> listaFuncionarios = fachada.listarTodosFuncionarios();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new String[]{"Matricula", "Nome","Setor"});
            for (int i = 0; i < listaFuncionarios.size(); i++) {
                modelo.addRow(new String[]{
                        listaFuncionarios.get(i).getMatricula() + ""
                        , listaFuncionarios.get(i).getNome()
                        ,listaFuncionarios.get(i).getSetor().getNome()});
            }
            jTableFuncionario.setModel(modelo);
        } catch (FuncionarioException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

