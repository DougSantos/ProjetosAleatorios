    private void TabelaListaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaListaMouseReleased
        try {
            
            int posicao = TabelaLista.getSelectedRow();
            Funcionario func = proxcamada.listarFunc().get(posicao);
            jTextCpf.setText(func.getCpf());
            jTextMat.setText(func.getMatricula() + "");
            jTextNome.setText(func.getNome());
            jTextRg.setText(func.getRg());
           PreecherTabela();
        } catch (Exception e) {
            ExibidorErro.setText("Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_TabelaListaMouseReleased

