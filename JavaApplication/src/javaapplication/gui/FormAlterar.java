    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int posicao = Integer.parseInt(jTextMat.getText());
            Funcionario func = new Funcionario();
            func.setCpf(jTextCpf.getText().trim());
            func.setMatricula(Integer.parseInt(jTextMat.getText().trim()));
            func.setNome(jTextNome.getText().trim().toUpperCase());
            func.setRg(jTextRg.getText().trim());
            proxcamada.alterarFuncionario(posicao, func);
            PreecherTabela();
            ExibidorErro.setText("Usu√°rio Alterado com Sucesso");
        } catch (Exception e) {
            ExibidorErro.setText("Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

