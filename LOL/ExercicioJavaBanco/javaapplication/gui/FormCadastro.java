    public Integer ValidarNum(String num) throws Exception {
        if (isInt(num) == false) {
            throw new Exception("Informe Apenas Numeros no Campo Matricula");
        }
        return Integer.parseInt(num);
        
    }
    
    public boolean isInt(String num) throws Exception {
        
        try {
            Integer.parseInt(num);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

