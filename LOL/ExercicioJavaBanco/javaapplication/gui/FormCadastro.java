    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        try {
            Funcionario func = new Funcionario();
            func.setCpf(jTextCpf.getText().trim());
            func.setMatricula(ValidarNum(jTextMatricula.getText().trim()));
            func.setNome(jTextNome.getText().trim().toUpperCase());
            func.setRg(jTextRG.getText().trim());
            proxCamada.cadastrarFuncionario(func);
            ExibidorErro.setText("Usuário Cadastrado com Sucesso");
        } catch (Exception e) {
            ExibidorErro.setText("Erro: " + e.getMessage());
        }
        
    }//GEN-LAST:event_CadastrarActionPerformed
    
